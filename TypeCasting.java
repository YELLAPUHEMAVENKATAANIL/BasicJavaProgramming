

public class TypeCasting {
    public static void main(String[] args) {

         // this is automatic Casting  int value converted to double
       int num = 10;
       double dvalue = num;
       System.out.println("Double value is "+dvalue); 


        // this is manual Casting 

        double dvalue2 = 20.0;
        int num2 =(int) dvalue2;
        System.out.println("Integer value is "+num2);

        
    }
    
}
